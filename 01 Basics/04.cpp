#include <iostream>
using namespace std;

int main()
{
  int x = 10;
  cout << x << endl;

  x = 9; // update x to 9
  cout << x << endl;

  x = x + 5; // update x to x + 5 (14)
  cout << x << endl;

  x = x - 2; // update x to x - 2 (12)
  cout << x << endl;

  x = x * 3; // update x to x * 3 (36)
  cout << x << endl;
}