#include <iostream>
using namespace std;

int main()
{
  int x = 10;
  int y = 3;

  cout << x + y << endl; // Output the sum of x and y (13)
  cout << x - y << endl; // Output the difference of x and y (7)
  cout << x * y << endl; // Output the product of x and y (30)
  cout << x / y << endl; // Output the quotient of x and y (3)
}